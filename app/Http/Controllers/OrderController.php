     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $orders = Order::with(['productType', 'status', 'diller'])->paginate(10);
        return view('admin.orders.index', compact('orders'));
    }

    /**
